def str_xor(secret, key):
    new_key = key * (len(secret) // len(key)) + key[:len(secret) % len(key)]
    encrypted_text = ""
    for secret_char, key_char in zip(secret, new_key):
        encrypted_text += chr(ord(secret_char) ^ ord(key_char))
    return encrypted_text

